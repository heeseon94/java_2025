package com.company.java005_ex;

public class ForEx003 {
	public static void main (String [] args) {
		
		//변수
		int sum = 0;

		//입력
		System.out.print("숫자를 입력하세요 : ");
		
		//처리
		//System.out.println(1 + "+" + 1 + "=" + 2);
		
		//출력
		for(int i=0; i<=10; i++) { 
			sum += i;
			}
		System.out.println(sum); 
		
		
		
	}

}
